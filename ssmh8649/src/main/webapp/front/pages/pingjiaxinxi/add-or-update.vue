			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																
																		// 自定义下拉框值
			this.pingfenOptions = "1,2,3,4,5".split(',')
																																							
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`pingjiaxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yuyuebianhao'){
					this.ruleForm.yuyuebianhao = obj[o];
					this.ro.yuyuebianhao = true;
					continue;
					}
					if(o=='pingfen'){
					this.ruleForm.pingfen = obj[o];
					this.ro.pingfen = true;
					continue;
					}
					if(o=='pingyu'){
					this.ruleForm.pingyu = obj[o];
					this.ro.pingyu = true;
					continue;
					}
					if(o=='changdibianhao'){
					this.ruleForm.changdibianhao = obj[o];
					this.ro.changdibianhao = true;
					continue;
					}
					if(o=='changdimingcheng'){
					this.ruleForm.changdimingcheng = obj[o];
					this.ro.changdimingcheng = true;
					continue;
					}
					if(o=='pingjiashijian'){
					this.ruleForm.pingjiashijian = obj[o];
					this.ro.pingjiashijian = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o];
					this.ro.fengmian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																
			// 多级联动参数
																																																
																																													
																																							// 日长控件选择日期时间
			pingjiashijianConfirm(val) {
				console.log(val)
				this.ruleForm.pingjiashijian = val.result;
				this.$forceUpdate();
			},
												
															// 下拉变化
			pingfenChange(e) {
				this.pingfenIndex = e.target.value
				this.ruleForm.pingfen = this.pingfenOptions[this.pingfenIndex]
			},
																																				
																																																															fengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fengmian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
									
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																				if(this.ruleForm.id){
					await this.$api.update(`pingjiaxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`pingjiaxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>


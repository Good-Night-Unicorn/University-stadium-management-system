	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												yuyuebianhao: '',
																pingfen: '',
																pingyu: '',
																changdibianhao: '',
																changdimingcheng: '',
																pingjiashijian: '',
																fengmian: '',
												},
																				pingfenOptions: [],
				pingfenIndex: 0,
																																																				// 登陆用户信息
				user: {},
                                ro:{
                                   yuyuebianhao : false,
                                   pingfen : false,
                                   pingyu : false,
                                   changdibianhao : false,
                                   changdimingcheng : false,
                                   pingjiashijian : false,
                                   fengmian : false,
